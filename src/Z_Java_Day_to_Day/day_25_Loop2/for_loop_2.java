package Z_Java_Day_to_Day.day_25_Loop2;

public class for_loop_2 {
    public static void main(String[] args) {
        System.out.println("Even number between 0-50 ");

        for (int sum=0;sum<=50;sum++){
            if (sum%2==0){
                System.out.println("sum even number is "+sum);
            }
        }




    }
}
