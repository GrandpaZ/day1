package Z_Java_Day_to_Day.day_20_String_manipulation;

public class contain_ {
    public static void main(String[] args) {

        String h="Hello word";
        System.out.println("Does it contain l "+h.contains("l"));
        System.out.println("Does it contain o "+h.contains("o"));
        System.out.println("Does it contain s "+h.contains("s"));
        System.out.println("Does it contain H "+h.contains("H"));

    }
}
