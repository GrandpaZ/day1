package Z_Java_Day_to_Day.day_31_A_rrays_2;

public class char_Arrays {
    public static void main(String[] args) {
        char[] c={'H','e','l','l','o',};
        for (char i:c) {
            System.out.print(i + " ");
        }
        String hello=new String(c);
        System.out.println("Hello = "+hello);
    }
}
