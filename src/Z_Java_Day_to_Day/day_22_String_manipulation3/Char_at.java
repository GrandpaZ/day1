package Z_Java_Day_to_Day.day_22_String_manipulation3;

public class Char_at {
    public static void main(String[] args) {
        String carBrand ="mitsubishi";
        System.out.println(carBrand.charAt(0));
        System.out.println(carBrand.charAt(5));
        System.out.println(""+carBrand.charAt(2)+carBrand.charAt(4)+carBrand.charAt(6));
    }
}
