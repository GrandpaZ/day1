package Z_Java_Day_to_Day.day5_primtives__concatanaton;

public class PrimtivesExample {
    public static void main(String[] args) {
        //Whole Number

        byte byteValue = 100;
        System.out.print("\n\tbyteValue = ");
        System.out.println(byteValue);
        short shortValue = 32000;
        System.out.print("\n\t ShortValue = ");
        System.out.println(shortValue);
        int intValue = 123456789;
        System.out.print("\n\t intValue = ");
        System.out.println(intValue);
        long longNumber = 12345678910l;
        System.out.print("\n\t longNumber = ");
        System.out.println(longNumber);

        //Decimal Number /Floating Point Type

        float floatValue=23.99f;
        System.out.print("\n\t floutValue = ");
        System.out.println(floatValue);
        double doubleValue=23.1234d;
        System.out.print("\n\t doubleValue = ");
        System.out.println(doubleValue);

        //********Character Type**********

        char  charValue='z';
        System.out.print("\n\t charValue = ");
        System.out.println(charValue);

        // Boolean = True of False

        boolean booleanValue= true;
        System.out.print("\n\t booleanValue = ");
        System.out.println(booleanValue);









    }

}
